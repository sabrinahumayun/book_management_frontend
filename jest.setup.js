// Mock Next.js use hook for React 19
jest.mock('react', () => {
  const actualReact = jest.requireActual('react')
  return {
    ...actualReact,
    use: (promise) => {
      if (promise && typeof promise.then === 'function') {
        return { id: '1' } // Mock resolved value
      }
      return promise
    },
  }
})

